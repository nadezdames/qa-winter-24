package HomeWork01;

public class Dogs {
    private String name = "noobscovich";
    private boolean fluffness = false;
    private int age = 14;
    private String colour = "Black";
    private double weight = 12.44;

    //---------GETTERS/SETTERS---------

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public boolean isFluffness() {
        return fluffness;
    }

    public void setFluffness(boolean fluffness) {
        this.fluffness = fluffness;
    }

    public int getAge() {
        return age;
    }

    public void setAge(int age) {
        this.age = age;
    }

    public String getColour() {
        return colour;
    }

    public void setColour(String colour) {
        this.colour = colour;
    }

    public double getWeight() {
        return weight;
    }

    public void setWeight(double weight) {
        this.weight = weight;
    }
}
